#include "libft.h"

char	*ft_strrchr(const char *str, int searchedChar)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
		i++;
	while (i >= 0)
	{
		if (str[i] == (char)searchedChar)
			return (&((char *)str)[i]);
		i--;
	}
	return (0);
}
