#include "libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	char	*mem;

	mem = malloc(size * nmemb);
	if (!mem)
		return (0);
	ft_memset(mem, 0, nmemb * size);
	return (mem);
}
